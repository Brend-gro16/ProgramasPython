# Verificar si un numero entero es positivo, negativo, o cero

print('\n----Verificar si un numero entero es positivo, negativo o cero----')

num = int(input('\n Introduce un numero: '))

if num > 0:
    print('\n\n ***El numero es positivo (+) ***')

if num < 0:
    print('\n\n ***El numero es negativo (-) ***')

if num == 0:
    print('\n\n ***El numero es cero***')